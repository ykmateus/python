// Declaração das notas e pesos
let n1 = 8.5;
let n2 = 7.0;
let n3 = 9.5;

let p1 = 2;
let p2 = 3;
let p3 = 5;

// Cálculo da média ponderada
let media = (n1 * p1 + n2 * p2 + n3 * p3) / (p1 + p2 + p3);

// Exibindo a média ponderada no console
console.log("A média ponderada é: " + media.toFixed(2));
